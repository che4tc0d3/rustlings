//Here the problem is that the first function is taking ownership of the String So
// `data` is moved and can't be used by `string_uppercase`

// To fix it you just need to change the right of access for each function, the first have read-only access and the second one have ownership

fn main() {
    let data = "Rust is great!".to_string();

    get_char(&data);

    string_uppercase(data);
}

// Should not take ownership
fn get_char(data: &String) -> char {
    data.chars().last().unwrap()
}

// Should take ownership
fn string_uppercase(mut data: String) {
    data = data.to_uppercase();

    println!("{}", data);
}
